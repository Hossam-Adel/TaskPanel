package dto;

public class Status {
	int ID,Check;
	String Name;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getCheck() {
		return Check;
	}
	public void setCheck(int check) {
		Check = check;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
}
